function add(a,b){
    console.log(a+b);
}
add(2,3);
//fat arrow function
let sub=(a,b)=>{
    console.log(a+b);
}
sub(2,3);

//returning values
function mul(a,b){
    return a+b
}
let n=mul(2,3)
console.log(n);
//fat arrow function with return value
let div=(a,b)=>{
    return a+b
}
let p=div(2,3)
console.log(p);
